    private int[] moneycount(int withdrawAmount){
        while (withdrawAmount != 0){
            if(withdrawAmount - 500 >= 0){
                withdrawAmount -= 500;
                banknotes[0] +=1;
            }else if(withdrawAmount - 200 >= 0){
                withdrawAmount -= 200;
                banknotes[1] +=1;
            }else if(withdrawAmount - 100 >= 0){
                withdrawAmount -= 100;
                banknotes[2] +=1;
            }else if(withdrawAmount - 50 >= 0){
                withdrawAmount -= 50;
                banknotes[3] +=1;
            }else{
                withdrawAmount -= 20;
                banknotes[4] +=1;
            }
        }
        return banknotes;
    }
