    private void send(){
        if(!message.getText().trim().equals("")){
            try {
                String ops = message.getText();
                String privateMsg = "/private " + reciver + " " + ops;
                outStream.write((privateMsg+"\n").getBytes(StandardCharsets.UTF_8));
                message.setText("");
            } catch (IOException e) {
                Logger.getLogger(Chat.class.getName()).log(Level.SEVERE, null, e);
            }
        }
    }
